import math
number=int(input("Enter a number:"))
square=math.sqrt(number)
fact=math.factorial(number)
power=math.pow(number,2)
print(f"Square root of {number}: {square}")
print(f"Factorial of {number}: {fact}")
print(f"{number} raised to the power of 2: {power} ")